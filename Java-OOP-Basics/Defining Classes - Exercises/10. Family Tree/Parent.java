package familyTree;


public class Parent {
    private String name;
    private String birthDateParent;

    public String getName() {
        return name;
    }

    public String getBirthDateParent() {
        return birthDateParent;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setBirthDateParent(String birthDateParent) {
        this.birthDateParent = birthDateParent;
    }
}